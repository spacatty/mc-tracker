export type TranslationKey = keyof typeof dictionary.en;

export function normalizeLocale(value: unknown): Locale {
  return value === "ru" ? "ru" : defaultLocale;
}

export function t(locale: Locale, key: TranslationKey) {
  return dictionary[locale][key] || dictionary.en[key];
}

export function roleLabel(locale: Locale, role: "owner" | "editor" | "viewer") {
  if (role === "owner") return t(locale, "owner");
  if (role === "editor") return t(locale, "editorRole");
  return t(locale, "viewerRole");
}
